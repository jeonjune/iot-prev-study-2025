# 변수

box = 'Apple' # 문자/문자열
print(box)

box = '파인애플' # 파이썬에서 문자열은 ' " 동일
print(box)

box = False # 참/거짓
print(box)

box = 1_000_000 # 정수, 쉼표는 안되고 _로 구분
print(box)

box = 3.14159265727 # 실수
print(box)

box = [1,2,3,4,5] # 리스트(배열)
print(box)

box = (1,2,3,4,5) # 튜플(특수한 리스트)
print(box)

box = [1, '2', True, [3,6,9],(11,22)] # 혼합리스트
print(box)

box = 1000000000000000000000000009090909090909090909090909090909090909090909090909
print(box)
